#include "ft_math.h"

void	matrix_identity(t_matrix *mat)
{
	mat->m[0][0] = 1;
	mat->m[0][1] = 0;
	mat->m[0][2] = 0;
	mat->m[0][3] = 0;
	;
	mat->m[1][0] = 0;
	mat->m[1][1] = 1;
	mat->m[1][2] = 0;
	mat->m[1][3] = 0;
	;
	mat->m[2][0] = 0;
	mat->m[2][1] = 0;
	mat->m[2][2] = 1;
	mat->m[2][3] = 0;
	;
	mat->m[3][0] = 0;
	mat->m[3][1] = 0;
	mat->m[3][2] = 0;
	mat->m[3][3] = 1;
}
