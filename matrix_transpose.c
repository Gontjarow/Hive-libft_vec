#include "ft_math.h"

t_matrix	matrix_transpose(t_matrix *mat)
{
	return ((t_matrix){
		mat->m[0][0],
		mat->m[1][0],
		mat->m[2][0],
		mat->m[0][1],
		mat->m[1][1],
		mat->m[2][1],
		mat->m[0][2],
		mat->m[1][2],
		mat->m[2][2]
	});
}
