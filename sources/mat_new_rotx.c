#include "ft_math.h"

t_matrix	mat_new_rotx(float radians)
{
	double c;
	double s;

	c = cos(radians);
	s = sin(radians);
	return ((t_matrix){
		.m[0][0] = 1, .m[0][1] = 0, .m[0][2] = 0, .m[0][3] = 0,
		.m[1][0] = 0, .m[1][1] = c, .m[1][2] = s, .m[1][3] = 0,
		.m[2][0] = 0, .m[2][1] = -s, .m[2][2] = c, .m[2][3] = 0,
		.m[3][0] = 0, .m[3][1] = 0, .m[3][2] = 0, .m[3][3] = 1
	});
}
