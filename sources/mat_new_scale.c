#include "ft_math.h"

t_matrix	mat_new_scale(double x, double y, double z)
{
	return ((t_matrix){.m = {
		{x, 0, 0, 0},
		{0, y, 0, 0},
		{0, 0, z, 0},
		{0, 0, 0, 1},
	}});
}
