#include "ft_math.h"

t_matrix	mat_new_scale_center(double x, double y, double z, t_xyz center)
{
	return ((t_matrix){
		.m[0][0] = x, .m[0][1] = 0, .m[0][2] = 0, .m[0][3] = 0,
		.m[1][0] = 0, .m[1][1] = y, .m[1][2] = 0, .m[1][3] = 0,
		.m[2][0] = 0, .m[2][1] = 0, .m[2][2] = z, .m[2][3] = 0,
		.m[3][0] = center.x * (1 - x),
		.m[3][1] = center.y * (1 - y),
		.m[3][2] = center.z * (1 - z),
		.m[3][3] = 1
	});
}
