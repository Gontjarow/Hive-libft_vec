#include "ft_math.h"

/*
** Rotates a matrix by a given quaternion.
*/

t_matrix	mat_rotate(t_matrix m, t_xyzw q)
{
	double c[9];

	c[0] = 1 - (q.y * (q.y * 2)) - (q.z * (q.z * 2));
	c[1] = (q.x * (q.y * 2)) - (q.w * (q.z * 2));
	c[2] = (q.x * (q.z * 2)) + (q.w * (q.y * 2));
	c[3] = (q.x * (q.y * 2)) + (q.w * (q.z * 2));
	c[4] = 1 - (q.x * (q.x * 2)) - (q.z * (q.z * 2));
	c[5] = (q.y * (q.z * 2)) - (q.w * (q.x * 2));
	c[6] = (q.x * (q.z * 2)) - (q.w * (q.y * 2));
	c[7] = (q.y * (q.z * 2)) + (q.w * (q.x * 2));
	c[8] = 1 - (q.x * (q.x * 2)) - (q.y * (q.y * 2));
	return ((t_matrix){.m = {
		{	m.m[0][0] * c[0] + m.m[0][1] * c[1] + m.m[0][2] * c[2],
			m.m[0][0] * c[3] + m.m[0][1] * c[4] + m.m[0][2] * c[5],
			m.m[0][0] * c[6] + m.m[0][1] * c[7] + m.m[0][2] * c[8], m.m[0][3] },
		{	m.m[1][0] * c[0] + m.m[1][1] * c[1] + m.m[1][2] * c[2],
			m.m[1][0] * c[3] + m.m[1][1] * c[4] + m.m[1][2] * c[5],
			m.m[1][0] * c[6] + m.m[1][1] * c[7] + m.m[1][2] * c[8], m.m[1][3] },
		{	m.m[2][0] * c[0] + m.m[2][1] * c[1] + m.m[2][2] * c[2],
			m.m[2][0] * c[3] + m.m[2][1] * c[4] + m.m[2][2] * c[5],
			m.m[2][0] * c[6] + m.m[2][1] * c[7] + m.m[2][2] * c[8], m.m[2][3] },
		{	m.m[3][0] * c[0] + m.m[3][1] * c[1] + m.m[3][2] * c[2],
			m.m[3][0] * c[3] + m.m[3][1] * c[4] + m.m[3][2] * c[5],
			m.m[3][0] * c[6] + m.m[3][1] * c[7] + m.m[3][2] * c[8], m.m[3][3] }
	}});
}
