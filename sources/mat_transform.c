t_matrix	mat_transform(t_matrix M, t_xyzw q)
{
	t_xyz q1;
	t_xyz q2;
	t_xyz q3;

	q1.x = 1 - (q.y * (q.y * 2)) - (q.z * (q.z * 2));
	q1.y = (q.x * (q.y * 2)) - (q.w * (q.z * 2));
	q1.z = (q.x * (q.z * 2)) + (q.w * (q.y * 2));
	q2.x = (q.x * (q.y * 2)) + (q.w * (q.z * 2));
	q2.y = 1 - (q.x * (q.x * 2)) - (q.z * (q.z * 2));
	q2.z = (q.y * (q.z * 2)) - (q.w * (q.x * 2));
	q3.x = (q.x * (q.z * 2)) - (q.w * (q.y * 2));
	q3.y = (q.y * (q.z * 2)) + (q.w * (q.x * 2));
	q3.z = 1 - (q.x * (q.x * 2)) - (q.y * (q.y * 2));
	return ((t_matrix){
		.m[0][3] = M.m[0][3], .m[1][3] = M.m[1][3],
		.m[2][3] = M.m[2][3], .m[3][3] = M.m[3][3],
		.m[0][0] = M.m[0][0] * q1.x + M.m[0][1] * q1.y + M.m[0][2] * q1.z,
		.m[0][1] = M.m[0][0] * q2.x + M.m[0][1] * q2.y + M.m[0][2] * q2.z,
		.m[0][2] = M.m[0][0] * q3.x + M.m[0][1] * q3.y + M.m[0][2] * q3.z,
		.m[1][0] = M.m[1][0] * q1.x + M.m[1][1] * q1.y + M.m[1][2] * q1.z,
		.m[1][1] = M.m[1][0] * q2.x + M.m[1][1] * q2.y + M.m[1][2] * q2.z,
		.m[1][2] = M.m[1][0] * q3.x + M.m[1][1] * q3.y + M.m[1][2] * q3.z,
		.m[2][0] = M.m[2][0] * q1.x + M.m[2][1] * q1.y + M.m[2][2] * q1.z,
		.m[2][1] = M.m[2][0] * q2.x + M.m[2][1] * q2.y + M.m[2][2] * q2.z,
		.m[2][2] = M.m[2][0] * q3.x + M.m[2][1] * q3.y + M.m[2][2] * q3.z,
		.m[3][0] = M.m[3][0] * q1.x + M.m[3][1] * q1.y + M.m[3][2] * q1.z,
		.m[3][1] = M.m[3][0] * q2.x + M.m[3][1] * q2.y + M.m[3][2] * q2.z,
		.m[3][2] = M.m[3][0] * q3.x + M.m[3][1] * q3.y + M.m[3][2] * q3.z
	});
}
